<h2>Barrierefreiheitserklärung</h2>
<br /><br />
<h3>Öffentliche Stelle / Geltungsbereich</h3>
<p><b>Landesamt für Gesundheit und Soziales (LAGeSo)</b></p>
<p>
  <br />Wir bemühen uns, unseren Webauftritt barrierefrei zu gestalten. Die Erklärung zur
  digitalen Barrierefreiheit wird im Gesetz über die barrierefreie Informations- und
  Kommunikationstechnik Berlin (BIKTG Bln) verlangt. Die technischen Anforderungen zur
  Barrierefreiheit ergeben sich aus der aktuellen BITV.<br />Diese Erklärung zur digitalen
  Barrierefreiheit gilt für das Angebot der Berliner Badestellen
  (badestellen.berlin.de).
</p>
<h3>Wann wurde die Erklärung zur Barrierefreiheit erstellt bzw. aktualisiert?</h3>
<p>Diese Erklärung wurde am 01.06.2020 erstellt bzw. überarbeitet.</p>
<p>
  Die Überprüfung der digitalen Barrierefreiheit wurde durchgeführt von der oben
  aufgeführten Stelle.
</p>
<h3>Wie barrierefrei ist das Angebot?</h3>
<p>
  Dieser Webauftritt / Diese Anwendung ist nur teilweise barrierefrei. Es werden nur
  teilweise die Anforderungen der aktuellen BITV (WCAG 2.0+) erfüllt.
</p>
<h3>Welche Bereiche sind nicht barrierefrei?</h3>
<p>
  Die nachstehend aufgeführten Bereiche sind aus folgenden Gründen nicht barrierefrei:
</p>
<h4>Unvereinbarkeit mit aktueller BITV</h4>
<p>Teilbereiche, die nicht barrierefrei sind:</p>
<h4>Barriere: Kartendarstellung</h4>
<p>
  Beschreibung: Das Kartenmaterial ist nicht vollständig nutzbar für Vorlesesoftware und
  Tastaturbedienbarkeit.
</p>
<p>
  Maßnahmen: Die Karte stellt eine alternative Navigation der Inhalte dar. Alle Inhalte
  der Seite können ohne die Nutzung der Karte erreicht und erschlossen werden.
</p>
<h4>Barriere: Visualisierungen der Messwerte</h4>
<p>
  Beschreibung: Die Messwerte werden als statische Visualisierungen in Form von SVG-Code
  dynamisch generiert. Eine barrierefreie Darstellung solcher Informationen, z.B. in Form
  von alternativ Text ist im klassischen Sinne nicht möglich.
</p>
<p>
  Maßnahmen: Die Rohdaten, welche die Grundlage der Visualisierung darstellen, werden zum
  Download angeboten.
</p>
<h4>Barriere: Videos</h4>
<p>
  Beschreibung: Das Erklärvideo, welches über die Video-Plattform YouTube eingebettet ist,
  gibt es nicht in einer alternativen Form.
</p>
<p>
  Maßnahme: Die Bilder des Videos dienen primär der visuellen Erklärung eines vorgelesenen
  Erklärtextes. Dieser vorgelesene Text, kann über den YouTube Video-Player als Untertitel
  hinzugeschaltet werden.
</p>
